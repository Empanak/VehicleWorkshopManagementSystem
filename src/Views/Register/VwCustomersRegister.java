private boolean isFilled(){
    if(txtName.getText().isEmpty()||
        txtLastNamePat.getText().isEmpty()||
        txtLastNameMat.getText().isEmpty()||
        txtFrmPhone.getText().replace("-", "").replace(" ", "").replace("_", "").isEmpty()){
            return false;
        }else{
            return true;
        }
}

